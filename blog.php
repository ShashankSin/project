          <h1>8 Steps to Authentic Leadership</h1>
          <p>Sooner or later every leader realizes that 99% of the people he depends on for success don’t report to him.  Authentic leadership relies on persuasion and persuasion relies on trust. Trust is the most important asset that any organization, brand, product, leader, or individual can have.  Most importantly, trust is not a scarce resource. We can all have more than we need.  However, trust is fragile: Once squandered it is often impossible to regain.  Here are 8 ways to guarantee that you, your product, your brand, and your company will always be trusted.</p>
          <p>Several months after taking a new job as   head of sales and marketing for a software company, the head of another   department came into my office and said, &ldquo;I think you have a commitment   problem.&rdquo;  She said she had given me several suggestions yet there was   no evidence that I was acting on any of them.</p>
          <p>&ldquo;I do have a commitment problem,&rdquo; I   replied. &ldquo;I am fanatical about keeping them.&rdquo; I told her that I never   commit to more than ten things at a time, and showed her a document   containing her suggestions as well as my ten top priorities. I said that   if she could make a case for replacing one of my priorities with one of   hers I would gladly listen.  She ended up agreeing with my priorities,   and this rocky start actually led to a deep personal friendship and the   most trusting and productive relationship of my entire career.</p>
          <p>Love may make the world go round, but   even love depends on trust.  Trust between the United States and Canada   produced the longest unguarded border in the history of mankind saving   untold billions in defense expenditures and economic friction.  I was in   Russia recently and a woman summed up Russia&rsquo;s tragic and enduring   problems with, &ldquo;There is no trust in Russia.&rdquo; We accept bits of   valueless paper called currency in exchange for goods and services   because we trust others to do the same, and I wouldn&rsquo;t dare climb into   my car if I didn&rsquo;t trust you to obey traffic laws.  Without trust the   world quickly descends into William James&rsquo; &ldquo;Bloomin&rsquo;, buzzin&rsquo;,   confusion.&rdquo;</p>
          <p>The first secret to trust is <strong>keeping promises</strong>.   Trust depends on promise and fulfillment or what lawyers call   contracts.  Few realize that corporate profit and loss statements   consist of promises not money.  There is no cash or &ldquo;real money&rdquo; on the   P&amp;L; instead the meat is accounts receivable and accounts payable   which are merely promises to pay for goods and services already rendered   or received.  In my own company we considered keeping promises so   mission critical that we implemented formal internal and external   processes for making, tracking, and fulfilling promises.  For example,   despite being a perpetually cash starved startup we paid our vendors on   time simply because this is what we had promised to do even if it meant I   didn&rsquo;t get a pay check.</p>
          <p>To build trust we also must <strong>be willing to make promises</strong> and this is the second step.  One of the most difficult management   challenges I ever encountered was getting others to use goal language.   We often assume that if we don&rsquo;t make promises we will never have to   worry about breaking them.  So we hide behind &ldquo;I&rsquo;ll try&rdquo; rather than   &ldquo;I&rsquo;ll do&rdquo; in an attempt to side step accountability through ambiguity,   equivalence, obfuscation, or even downright double talk.  Unfortunately   this stratagem merely signals a person who can&rsquo;t trust himself, and   someone who can&rsquo;t trust himself is never trusted by others.  We all want   solid commitments from others and this means we must be willing to   offer accountability ourselves.</p>
          <p>The third secret to establishing trust is to <strong>under commit and over deliver</strong>.    Over promising is the flip side to under promising and just as   damaging. Negotiating up front is far more effective in creating and   maintaining trust than the inevitable excuses that arise when a promise   is not fulfilled. We overcommit because we want others to like us, but   the best way to ruin a relationship is by not following through on our   promises.</p>
          <p>The fourth secret to trust is <strong><em>proactive</em> communication</strong>. I recently read an article in the <a href="http://www.forbes.com/wall-street/">Wall Street</a> Journal (don&rsquo;t ask why!) evaluating various firms that clean and   preserve bridal dresses.  An expensive process that takes months, one   firm dazzled the article&rsquo;s author by sending weekly progress reports. As   fallible human beings we will never keep all our promises, but no   matter how compelling our excuses may be for failing to deliver on time   and on budget there is no excuse for not giving others a heads up.     Keeping everyone in the loop is not just common courtesy it is   essential.  If the news is good people can relax, and if the news is bad   there is plenty of time to go to plan B. In the story I related above,   my mistake was in not communicating therefore forcing my colleague to   confront me in the first place about her suggestions.</p>
          <p>The fifth secret is <strong>don&rsquo;t cut corners</strong>.   We are far better off doing a few critical things to the nth degree   than a lot of things with a &ldquo;good enough&rdquo; attitude. Cutting corners   assumes that no one will notice, and this adds insult to injury because   it implicitly treats others as if they were too damn stupid to notice.</p>
          <p>The sixth key to trust is <strong>never hide mistakes</strong>.    Inevitably some of our mistakes will be discovered and when they are   any rational observer will assume that where there is one there are   others yet to be discovered.  Autonomy or being our own boss is what   turns work into fun, and hiding mistakes is the quickest way to trade   hands off autonomy for a hands on boss or regulatory agency anxiously   monitoring our every move.  Also beware of excuses; a simple apology is   far more effective in maintaining trust than a box car full of trumped   up excuses and finger pointing.</p>
          <p>The seventh trust building secret is <strong>purifying motivations</strong>.    Putting our own agenda first always dooms trust.  Others rapidly see   through the smoke screen, and from then on every move we make is   screened for ulterior motivations real or imagined.  Conversely, team   players who put the interests of others ahead of their own accrue the   incredible power that only trust can bestow.</p>
          <p>The final secret to trust is <strong>never make people ask</strong>.    When you repay a debt or fulfill an obligation without being reminded   you get ten times the trust building credit that you would otherwise.     Nobody likes hounding a company or an individual to fulfill a promise,   and when you add this emotional cost it is always subtracted from your   treasury of trust.  For example, when your friends must remind you to   pick up the check once in a while you end up picking up more than your   share of the checks while getting little or no credit for the gesture.    When I forced my colleague to come to me about her suggestions I was   guilty of violating this simple rule which put me on the defensive.</p>
